from math import pi

def kwadrat(x):
    print(f"{x * x}")

def prostokat(x, y):
    print(f"{x * y}")

def trojkat(x, y):
    print(f"{x * y / 2}")

def trapez(x, y, z):
    print(f"{x + y * z / 2}")

def romb(x, y):
    print(f"{x * y}")

def kolo(x):
    print(f"{pi * (x ** 2)}")

